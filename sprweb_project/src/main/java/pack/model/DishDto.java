package pack.model;

public class DishDto {
	private String menu_no, menu_name, menu_price, menu_sdate, menu_category, menu_image, menu_count;
	

	public String getMenu_count() {
		return menu_count;
	}

	public void setMenu_count(String menu_count) {
		this.menu_count = menu_count;
	}

	public String getMenu_no() {
		return menu_no;
	}

	public void setMenu_no(String menu_no) {
		this.menu_no = menu_no;
	}

	public String getMenu_name() {
		return menu_name;
	}

	public void setMenu_name(String menu_name) {
		this.menu_name = menu_name;
	}

	public String getMenu_price() {
		return menu_price;
	}

	public void setMenu_price(String menu_price) {
		this.menu_price = menu_price;
	}

	public String getMenu_sdate() {
		return menu_sdate;
	}

	public void setMenu_sdate(String menu_sdate) {
		this.menu_sdate = menu_sdate;
	}

	public String getMenu_category() {
		return menu_category;
	}

	public void setMenu_category(String menu_category) {
		this.menu_category = menu_category;
	}

	public String getMenu_image() {
		return menu_image;
	}

	public void setMenu_image(String menu_image) {
		this.menu_image = menu_image;
	}
	
	
}
