package pack.model;

public class ReviewDto {
	private String review_no, review_id, review_comment, review_sdate, review_menu_num, menu_name, menu_category;

	public String getReview_no() {
		return review_no;
	}

	public void setReview_no(String review_no) {
		this.review_no = review_no;
	}

	public String getReview_id() {
		return review_id;
	}

	public void setReview_id(String review_id) {
		this.review_id = review_id;
	}

	public String getReview_comment() {
		return review_comment;
	}

	public void setReview_comment(String review_comment) {
		this.review_comment = review_comment;
	}

	public String getReview_sdate() {
		return review_sdate;
	}

	public void setReview_sdate(String review_sdate) {
		this.review_sdate = review_sdate;
	}

	public String getReview_menu_num() {
		return review_menu_num;
	}

	public void setReview_menu_num(String review_menu_num) {
		this.review_menu_num = review_menu_num;
	}

	public String getMenu_name() {
		return menu_name;
	}

	public void setMenu_name(String menu_name) {
		this.menu_name = menu_name;
	}

	public String getMenu_category() {
		return menu_category;
	}

	public void setMenu_category(String menu_category) {
		this.menu_category = menu_category;
	}
	
	
	
	
}
