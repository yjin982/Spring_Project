package pack.model;

public class CartBean {
	private String member_id, menu_no, menu_name, menu_price, menu_quantity, set_category;

	public String getMember_id() {
		return member_id;
	}

	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}

	public String getMenu_no() {
		return menu_no;
	}

	public void setMenu_no(String menu_no) {
		this.menu_no = menu_no;
	}

	public String getMenu_name() {
		return menu_name;
	}

	public void setMenu_name(String menu_name) {
		this.menu_name = menu_name;
	}

	public String getMenu_price() {
		return menu_price;
	}

	public void setMenu_price(String menu_price) {
		this.menu_price = menu_price;
	}

	public String getMenu_quantity() {
		return menu_quantity;
	}

	public void setMenu_quantity(String menu_quantity) {
		this.menu_quantity = menu_quantity;
	}

	public String getSet_category() {
		return set_category;
	}

	public void setSet_category(String set_category) {
		this.set_category = set_category;
	}
}
